COMMENT ON COLUMN TRAVELSAVE.IDX IS '회원번호';

COMMENT ON COLUMN TRAVELSAVE.CONTENTID IS '여행지번호';

COMMENT ON COLUMN TRAVELSAVE.SDATE IS '찜한날짜';

COMMENT ON COLUMN TRAVELSAVE.TITLE IS '여행지제목';

