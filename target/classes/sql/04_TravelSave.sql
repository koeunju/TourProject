/* 여행지찜 */
CREATE TABLE TRAVELSAVE (
	IDX NUMBER(10) NOT NULL, /* 회원번호 */
	CONTENTID NUMBER(10) NOT NULL, /* 여행지번호 */
	SDATE DATE /* 찜한날짜 */
	TITLE VARCHAR2(500) /* 여행지 제목 */
);

